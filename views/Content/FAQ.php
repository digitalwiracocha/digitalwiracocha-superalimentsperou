Très bien, voici quelques exemples de questions fréquemment posées (FAQ) adaptées à votre projet :

1. **Qu'est-ce que les superaliments ?** 
   Les superaliments sont des aliments naturels qui sont riches en nutriments et ont des bienfaits pour la santé. Nos superaliments proviennent directement du Pérou et sont produits de manière durable et équitable.

2. **D'où proviennent vos produits ?**
   Nos produits sont cultivés au Pérou, dans différentes régions : la Maca de Junin, le Camu Camu de Pucallpa, le Sacha Inchi près de Machu Picchu et la Graviola et Moringa de Quillabamba. Chacun de ces produits est récolté et transformé localement pour préserver toutes ses qualités nutritives.

3. **Comment vos produits sont-ils produits ?**
   Nos produits sont récoltés et transformés manuellement par des coopératives agricoles locales au Pérou. Le processus respecte les principes de l'agriculture biologique et vise à préserver la nature.

4. **Qu'est-ce que le commerce équitable ?**
   Le commerce équitable est un système commercial basé sur le dialogue, la transparence et le respect, qui cherche à établir un équilibre entre profit et responsabilité sociale. En achetant nos produits, vous contribuez directement à soutenir les communautés autochtones du Pérou et à préserver leurs traditions et leurs savoirs ancestraux.

5. **Comment soutenez-vous la communauté Qeros ?**
   Nous travaillons en étroite collaboration avec la communauté Qeros pour soutenir leur économie locale, améliorer leurs conditions de vie et préserver leurs traditions et savoirs ancestraux. Une partie des profits de nos ventes est directement réinvestie dans des initiatives de soutien à la communauté Qeros.

6. **Comment puis-je participer ou aider davantage ?**
   En plus d'acheter nos produits, vous pouvez nous aider en participant à nos voyages solidaires, en faisant un don ou en partageant notre mission avec votre entourage. Toute aide est la bienvenue pour soutenir nos actions en faveur des populations natives du Pérou.

7. **Vos produits sont-ils biologiques ?**
   Oui, tous nos produits sont biologiques, sans additifs ni conservateurs. Nous respectons strictement les normes de l'agriculture biologique pour vous fournir des produits de la plus haute qualité.

8. **Comment utiliser vos produits ?**
   Nos produits peuvent être utilisés de différentes façons, selon vos goûts et vos préférences. Vous pouvez les ajouter à vos smoothies, vos yaourts, vos salades ou les consommer tels quels. Vous trouverez de nombreuses idées de recettes sur notre site.

9. **Comment sont expédiées mes commandes ?**
   Nous expédions nos produits dans le monde entier. Les frais de port dépendent de votre lieu de résidence. Nous nous efforçons de traiter et d'expédier les commandes dans les plus brefs délais.

