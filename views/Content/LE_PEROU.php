# **Le Pérou**

Le Pérou est un pays d'une richesse extraordinaire, tant sur le plan de son histoire, de ses paysages que de sa culture. C'est la terre de civilisations anciennes, de sommets enneigés des Andes, de vallées fertiles et de forêts amazoniennes luxuriantes. C'est dans ce contexte unique que poussent nos super aliments, nourris par la nature généreuse et les savoirs ancestraux.

## **Cuzco et Quillabamba**

Cuzco, ancienne capitale de l'Empire Inca, est aujourd'hui une ville dynamique, un carrefour entre le passé et le présent. C'est le point de départ vers la célèbre cité inca du Machu Picchu, mais c'est aussi une région d'une grande richesse agricole. Ses terres fertiles abritent la culture de la maca, réputée pour ses propriétés nutritives.

Quillabamba, situé dans une vallée de la jungle, est une région bénie par la nature. Elle est connue pour la culture de la graviola et du moringa, super aliments reconnus pour leurs bienfaits sur la santé.

## **Les Andes**

La cordillère des Andes, colonne vertébrale du Pérou, est le berceau de cultures ancestrales. Les communautés locales ont su, au fil des siècles, s'adapter à ces conditions difficiles et exploiter les ressources naturelles de façon durable. C'est dans ces montagnes que pousse le Sacha Inchi, une plante aux graines précieuses, riches en oméga 3, 6 et 9.

## **Le Pérou au 21ème siècle**

Aujourd'hui, l'économie péruvienne repose en grande partie sur l'agriculture et le tourisme. Ces secteurs, pourtant porteurs de développement, sont aussi confrontés à de nombreux défis. Les problématiques d'équité sociale, de préservation de la biodiversité et de lutte contre le changement climatique sont au cœur de nos préoccupations. C'est pourquoi nous nous engageons activement pour un commerce équitable, pour soutenir les communautés locales et pour préserver les ressources naturelles du Pérou.

Le Pérou est bien plus qu'un pays, c'est une terre d'histoire et de tradition, une terre de richesse naturelle, une terre qui nous nourrit et que nous nous devons de respecter et de protéger.

