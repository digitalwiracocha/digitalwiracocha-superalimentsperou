# **Nos produits**

## **Une sélection rigoureuse de Super aliments du Pérou**

Chez Amazon Andes France, nous vous proposons une sélection exclusive de super aliments organiques, directement importés des régions de production du Pérou. Découvrez nos produits et leurs incroyables bienfaits pour la santé.

**Camu Camu Bio - Pucallpa**
Cultivé par la communauté Shipibos dans la région de Pucallpa, notre Camu Camu bio est un trésor d'antioxydants. Ce fruit d'Amazonie est l'un des plus riches en vitamine C, stimulant le système immunitaire et contribuant à une peau éclatante. 

**Maca Bio - Junín**
Provenant de la région de Junín, notre Maca bio est cultivée en haute altitude. Reconnue pour ses propriétés adaptogènes, elle aide à augmenter l'énergie et l'endurance, soutient l'équilibre hormonal et améliore la libido.

**Sacha Inchi Bio - Machu Picchu**
Récolté dans la région de Machu Picchu, notre Sacha Inchi bio est une excellente source d'oméga-3, oméga-6 et oméga-9. Connu pour soutenir la santé cardiaque et le cerveau, il est également riche en protéines, en fibres et en antioxydants.

**Graviola Bio - Quillabamba**
Cultivée dans la région de Quillabamba, notre Graviola bio est reconnue pour ses propriétés antioxydantes et anti-inflammatoires. Traditionnellement utilisée en médecine naturelle, elle est souvent consommée pour renforcer le système immunitaire et favoriser la relaxation.

**Moringa Bio - Quillabamba**
Également issue de la région de Quillabamba, notre Moringa bio est une super plante aux multiples vertus. Riche en vitamines, minéraux et acides aminés, elle est reconnue pour stimuler le système immunitaire, favoriser la digestion et soutenir la santé des os.

Tous nos produits sont récoltés manuellement, en harmonie avec la nature, et transformés à froid, sans additifs. Cette méthode de production préserve toutes leurs propriétés bénéfiques pour votre santé.

Chez Amazon Andes France, nous vous proposons non seulement des produits naturels de haute qualité, mais nous vous invitons aussi à découvrir le Pérou, ses cultures ancestrales et sa biodiversité exceptionnelle.

