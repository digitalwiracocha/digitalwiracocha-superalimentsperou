# **Nos actions**

## **Protéger - Préserver - Partager**

Chez Amazon Andes France, notre mission ne s'arrête pas à la vente de produits naturels de haute qualité. Nous sommes engagés dans une démarche solidaire et responsable vis-à-vis des communautés natives du Pérou, en particulier la Nation Qeros. Nos actions s'articulent autour de trois piliers principaux : Protéger, Préserver et Partager.

### **Protéger**

Nous nous efforçons de protéger les familles et les enfants de la Nation Qeros, une communauté en danger d'extinction. Nous agissons contre la malnutrition qui touche les enfants, en organisant des campagnes médicales urgentes pour l'ORL, la dentition et la gynécologie. Nous améliorons également les conditions de vie des familles en mettant en place des systèmes de purification de l'eau, des panneaux solaires et en améliorant leurs habitats. 

### **Préserver**

Notre engagement s'étend à la préservation des traditions et du savoir-faire ancestral de la Nation Qeros. Nous aidons à développer une économie durable et équitable en favorisant la vente de leur artisanat. Pour ce faire, nous créons des ateliers de production et organisons des voyages solidaires en immersion pour découvrir leur culture. Nous collectons également les histoires et les contes des derniers anciens et chamans avant qu'ils ne se perdent.

### **Partager**

Nous partageons les principes de la cosmovision andine qui peuvent être salutaires pour notre civilisation face aux crises que nous vivons. Nous soutenons l'éducation et la formation des jeunes et aidons au développement agricole pour garantir leur autonomie alimentaire. 

En plus de ces actions, nous contribuons à la préservation de l'environnement en soutenant la reforestation et la création d'un jardin botanique avec des plantes endémiques. 

Notre objectif est de créer un cercle vertueux de commerce équitable, de protection sociale et de préservation culturelle, tout en vous offrant des super aliments de haute qualité.

Rejoignez-nous dans cette aventure solidaire !

