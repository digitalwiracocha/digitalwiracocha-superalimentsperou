# **Qui sommes-nous**

## **Amazon Andes France : Un lien direct entre les super aliments du Pérou et vous**

Nés d'une passion pour la nature abondante et les cultures ancestrales du Pérou, nous, à Amazon Andes France, avons passé deux décennies immergés dans la vie et les traditions des peuples autochtones des Andes et de la jungle amazonienne.

Notre histoire commence avec notre voyage dans les montagnes majestueuses des Andes, les marais luxuriants de l'Amazonie et les vallées fertiles où prospèrent les plantes médicinales traditionnelles. Ce sont ces terres sacrées qui nous ont donné le Maca, le Camu Camu, et le Sacha Inchi - des super aliments qui sont au cœur de notre offre.

Nous avons partagé des moments précieux avec les peuples Qeros, Shipibos, Ashaninka et Matsikenga, appris de leurs sagesse ancestrale, leurs connaissances des plantes et leurs médecines naturelles. Leur profonde connexion avec la nature a éclairé notre propre chemin, et nous a inspiré à créer une entreprise qui respecte et préserve ces traditions.

## **Notre mission**

Chez Amazon Andes France, nous avons pour mission d'apporter les super aliments bio du Pérou à votre porte. Mais notre engagement ne se limite pas à la qualité des produits que nous offrons. Nous œuvrons pour un commerce équitable et responsable, soutenant les communautés natives, préservant leurs cultures ancestrales et aidant les enfants du Pérou.

## **Rejoignez notre aventure**

En faisant vos achats chez Amazon Andes France, vous ne vous contentez pas de choisir des aliments sains et nutritifs. Vous rejoignez une aventure passionnante, celle d'un lien direct avec les communautés natives du Pérou, un soutien à l'agriculture biologique et à la conservation de la biodiversité.

Nous vous invitons à explorer les paysages sacrés du Pérou, à découvrir ses super aliments, à partager nos valeurs et à faire partie de notre voyage. Rejoignez-nous chez Amazon Andes France, votre passerelle vers les super aliments du Pérou.

