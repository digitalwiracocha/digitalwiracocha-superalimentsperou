# **Les Qeros**

Les Qeros sont une communauté autochtone qui vit dans la cordillère des Andes, au Pérou. Reconnus comme les derniers descendants directs de l'empire Inca, ils conservent un mode de vie ancestral, en harmonie avec la nature. Malgré les défis du 21ème siècle, les Qeros perpétuent leurs traditions, leur langue et leurs savoirs ancestraux, qui sont une véritable source d'inspiration pour notre entreprise.

## **Le mode de vie des Qeros**

Vivre dans les hauteurs des Andes exige courage et résilience. Les Qeros, en harmonie avec leur environnement, ont su tirer parti de la nature environnante, en cultivant des tubercules et des céréales à des altitudes où peu de cultures peuvent prospérer. Leur alimentation est complétée par l'élevage de lamas et d'alpagas, animaux emblématiques de la région.

## **Les savoirs ancestraux**

Les Qeros sont dépositaires d'une riche tradition orale et de savoirs ancestraux. Leurs connaissances des plantes médicinales, transmises de génération en génération, sont une source inestimable d'informations sur les super aliments que nous proposons. Ils nous ont appris à respecter et à travailler avec la nature, à récolter de manière éthique et à préparer les aliments de manière à préserver leurs propriétés nutritionnelles.

## **Les défis du 21ème siècle**

Les Qeros, malheureusement, ne sont pas épargnés par les défis du 21ème siècle. Le changement climatique, la déforestation et la perte de la biodiversité menacent leur mode de vie traditionnel. De plus, ils sont confrontés à des problèmes de santé et d'accès aux services de base. C'est pour soutenir cette communauté et préserver leur culture unique que nous avons mis en place des initiatives de commerce équitable, d'éducation et de soutien aux soins de santé.

En achetant nos produits, vous contribuez directement à améliorer les conditions de vie des Qeros et à préserver un héritage culturel inestimable.

